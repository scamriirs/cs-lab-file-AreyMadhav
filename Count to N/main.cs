
// Using a for Loop
using System;

class Program
{
    static void Main()
    {
        int n = 10; // Replace with your desired value
        for (int i = 1; i <= n; i++)
        {
            Console.WriteLine(i);
        }
    }
}

// Using a while Loop
using System;

class Program
{
    static void Main()
    {
        int n = 10; // Replace with your desired value
        int i = 1;
        while (i <= n)
        {
            Console.WriteLine(i);
            i++;
        }
    }
}

// Using a do-while Loop
using System;

class Program
{
    static void Main()
    {
        int n = 10; // Replace with your desired value
        int i = 1;
        do
        {
            Console.WriteLine(i);
            i++;
        } while (i <= n);
    }
}
